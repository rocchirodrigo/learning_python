# This file should contain all the four basic operations.

def soma(x: float, y: float) -> float:
    """
    Reads two numbers and return their sum.
    :param x: First number
    :param y: Second number
    :return: value of the sum of `x` and `y`.
    """
    return x + y


def subtraction(x: float, y: float) -> float:
    """
    Reads two numbers and return their subtraction.
    :param x: First number
    :param y: Second number
    :return: value of the difference between `x` and `y`.
    """
    return x - y


def multiplication(x: float, y: float) -> float:
    """
    Reads two numbers and return their product.
    :param x: First number
    :param y: Second number
    :return: value of the product between `x` and `y`.
    """
    return x * y


def division(x: float, y: float) -> float:
    """
    Reads two numbers and return their quotient.
    :param x: First number
    :param y: Second number
    :return: value of the division between `x` and `y`.
    """
    return x / y
